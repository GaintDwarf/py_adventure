class Ship(object):
    """
    Ship class.

    Properties: 
        - width  - the width of the ship.
        - legnth - the length of the ship.
    """

    def __init__(self, width: int, legnth: int):
        """
        :param width: the width of the ship
        :type width: int
        :param legnth: the legnth of the ship
        :type legnth: int
        """
        self.width  = width
        self.legnth = legnth

